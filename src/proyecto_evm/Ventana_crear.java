    private void botonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCrearActionPerformed
        String textoNombre = Text_Nombre_Proyecto.getText();
        String textoNumeroHito = Text_Numero_Hito.getText();
        String textoBAC = text_BAC.getText(); // Corregir la asignación aquí
        boolean prorrateado;

        if (textoNombre.isEmpty() || textoBAC.isEmpty() || textoNumeroHito.isEmpty() || (!opcionProrrateado.isSelected() && !opcionNoProrrateado.isSelected())) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos correctamente y seleccione una opción de prorrateo.");
            return;
        }

        // Verificar si ya existe un proyecto con el mismo nombre
        if (gestorProyectos.existeProyecto(textoNombre)) {
            JOptionPane.showMessageDialog(this, "Ya existe un proyecto con el mismo nombre. Por favor, elija un nombre único.");
            return;
        }

        try {
            int numeroHito = Integer.parseInt(textoNumeroHito);
            float BAC = Float.parseFloat(textoBAC);
            if (numeroHito <= 0 || BAC <= 0) {
                throw new IllegalArgumentException("El número de hitos y el BAC deben ser mayores que 0.");
            }

            if (opcionProrrateado.isSelected()) {
                prorrateado = true;
            } else {
                prorrateado = false;
            }

            boolean proyectoCreado = gestorProyectos.crearProyecto(textoNombre, numeroHito, prorrateado, BAC);

            if (proyectoCreado) {
                JOptionPane.showMessageDialog(this, "Proyecto creado exitosamente.");
            } else {
                JOptionPane.showMessageDialog(this, "Error al crear el proyecto.");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese datos validos.");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error en la creación del proyecto", JOptionPane.ERROR_MESSAGE);
        }



    }//GEN-LAST:event_botonCrearActionPerformed

