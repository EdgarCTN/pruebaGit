    private void tablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaDatosMouseClicked
        int columnaBotonEliminar = 3; // La columna del botón "Eliminar" en tu modelo de tabla
        int columnaBotonModificar = 4; // La columna del botón "Modificar" en tu modelo de tabla

        // Verificar si el clic se hizo en la columna del botón "Eliminar"
        if (tablaDatos.columnAtPoint(evt.getPoint()) == columnaBotonEliminar) {
            int fila = tablaDatos.rowAtPoint(evt.getPoint());
            if (fila != -1) {
                String nombreProyecto = (String) tablaDatos.getValueAt(fila, 0);

                // Eliminar el proyecto del gestor
                boolean eliminado = gestorProyectos.eliminarProyecto(nombreProyecto);

                if (eliminado) {
                    JOptionPane.showMessageDialog(null, "Proyecto eliminado exitosamente.");
                    // Actualizar la tabla después de eliminar
                    actualizarTablaDatos();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al eliminar el proyecto.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un proyecto para eliminar.");
            }
        }
        // Verificar si el clic se hizo en la columna del botón "Modificar"
        else if (tablaDatos.columnAtPoint(evt.getPoint()) == columnaBotonModificar) {
            int fila = tablaDatos.rowAtPoint(evt.getPoint());
            if (fila != -1) {
                String nombreProyecto = (String) tablaDatos.getValueAt(fila, 0);
                TablaHitos newframe = new TablaHitos(gestorProyectos, nombreProyecto);
                newframe.setVisible(true);
                this.dispose();
            }
        }

    }//GEN-LAST:event_tablaDatosMouseClicked

