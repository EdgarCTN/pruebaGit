    private void actualizarTabla() {
        String[][] datos = gestorProyectos.obtenerDatosProyectos();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos

        for (String[] proyecto : datos) {
            model.addRow(proyecto);
        }
    }
    private void configurarTabla() {
        jTable2.setDefaultEditor(Object.class, null); // Hace que todas las celdas no sean editables

        // Desactiva la reorganización de columnas
        jTable2.getTableHeader().setReorderingAllowed(false);
    }

