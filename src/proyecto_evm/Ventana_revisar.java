    private void botonRevisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRevisarActionPerformed
        try {
            String nombreProyecto = Text_Nombre_Proyecto.getText();

            if (nombreProyecto.isEmpty()) {
                throw new IllegalArgumentException("El nombre del proyecto no puede estar vacío.");
            }

            int numeroHito = Integer.parseInt(Text_Numero_Hito.getText());

            if (numeroHito <= 0) {
                throw new IllegalArgumentException("El número de hito debe ser un entero positivo.");
            }

            int variacion = botonTipicas.isSelected() ? 1 : (botonNoTipicas.isSelected() ? 2 : 0);

            String[][] resultados = gestorProyectos.revisarProyecto(nombreProyecto, numeroHito, variacion);

            if (resultados != null) {
                TablaAnalisis tablaAnalisis = new TablaAnalisis(gestorProyectos, resultados);
                tablaAnalisis.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudieron obtener resultados para el proyecto.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un número válido para el Hito.", "Error de formato", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error de entrada", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error inesperado.", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_botonRevisarActionPerformed

